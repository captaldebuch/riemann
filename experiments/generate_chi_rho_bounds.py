"""Generate Arb-backed rational intervals for the χ–ρ inner products.

The generated Lean files keep the external trust boundary at the primitive
transcendental facts `log n` (2 ≤ n ≤ 50).  Every interval for
`innerProductChiRho k` is then proved in Lean from `innerProductChiRho_formula`,
the generated log bound, and the existing Arb bound for Euler's constant.

Run with the pyenv Python that provides python-flint:

    ~/.pyenv/shims/python3 experiments/generate_chi_rho_bounds.py
"""

from __future__ import annotations

import argparse
import hashlib
import json
import math
from fractions import Fraction
from pathlib import Path

import flint
import numpy as np


ROOT = Path(__file__).resolve().parents[1]
GENERATED = ROOT / "RiemannHypothesis" / "Certificates" / "Generated"
SIZES = (10, 20, 50)
LOG_DENOMINATOR = 10**15
LINEAR_RADIUS = Fraction(1, 10**9)
ARB_PRECISION = 256


def lean_fraction(value: Fraction) -> str:
    if value.denominator == 1:
        return str(value.numerator)
    return f"({value.numerator} / {value.denominator})"


def outward_log_bounds(n: int) -> tuple[Fraction, Fraction]:
    value = flint.arb(n).log()
    lower_num = math.floor(float(value.lower()) * LOG_DENOMINATOR)
    upper_num = math.ceil(float(value.upper()) * LOG_DENOMINATOR)
    while not value >= flint.fmpq(lower_num, LOG_DENOMINATOR):
        lower_num -= 1
    while not value <= flint.fmpq(upper_num, LOG_DENOMINATOR):
        upper_num += 1
    return (
        Fraction(lower_num, LOG_DENOMINATOR),
        Fraction(upper_num, LOG_DENOMINATOR),
    )


def linear_centers() -> list[Fraction]:
    gamma = float(np.euler_gamma)
    return [
        Fraction((math.log(k + 1) + 1.0 - gamma) / (k + 1)).limit_denominator(10**12)
        for k in range(50)
    ]


def audit_data(
    centers: list[Fraction], log_bounds: dict[int, tuple[Fraction, Fraction]]
) -> None:
    gamma = flint.arb.const_euler()
    for n, (lower, upper) in log_bounds.items():
        value = flint.arb(n).log()
        assert value >= flint.fmpq(lower.numerator, lower.denominator)
        assert value <= flint.fmpq(upper.numerator, upper.denominator)
    for k, center in enumerate(centers):
        exact = (flint.arb(k + 1).log() + 1 - gamma) / (k + 1)
        lower = center - LINEAR_RADIUS
        upper = center + LINEAR_RADIUS
        assert exact >= flint.fmpq(lower.numerator, lower.denominator)
        assert exact <= flint.fmpq(upper.numerator, upper.denominator)


def vector_definition(name: str, size: int, values: list[Fraction]) -> str:
    lines = [f"def {name} (i : Fin {size}) : ℚ :="]
    for i, value in enumerate(values[:size]):
        prefix = "if" if i == 0 else "else if"
        lines.append(f"  {prefix} i.val == {i} then {lean_fraction(value)}")
    lines.append("  else 0")
    return "\n".join(lines)


def interval_statement(size: int, k: int) -> str:
    return f"""theorem innerProductChiRho_interval_{k} :
    ((linearLower ({k} : Fin {size}) : ℚ) : ℝ) ≤ innerProductChiRho {k} ∧
      innerProductChiRho {k} ≤ ((linearUpper ({k} : Fin {size}) : ℚ) : ℝ) := by"""


def emit_primary(
    centers: list[Fraction], log_bounds: dict[int, tuple[Fraction, Fraction]]
) -> str:
    lines = [
        "import RiemannHypothesis.Certificates.NymanBeurlingCertificate",
        "import RiemannHypothesis.Certificates.Generated.TranscendentalBoundsGenerated",
        "import RiemannHypothesis.Certificates.IntervalCertificate",
        "",
        "/-! Arb-generated primitive log bounds and Lean-derived χ–ρ intervals for N=50. -/",
        "",
        "set_option linter.style.longLine false",
        "set_option maxHeartbeats 2000000",
        "",
        "namespace RH.Certificates.Generated.ChiRhoBoundsN50",
        "",
        "open RH.Certificates",
        "open RH.Certificates.Generated",
        "",
        "-- Generated by experiments/generate_chi_rho_bounds.py using python-flint (Arb).",
        "-- The axioms below are the explicit external certificate boundary.",
        "",
    ]
    for n in range(2, 51):
        lower, upper = log_bounds[n]
        lines.extend(
            [
                f"def log_{n}_lower : ℚ := {lean_fraction(lower)}",
                f"def log_{n}_upper : ℚ := {lean_fraction(upper)}",
                "",
                f"axiom log_{n}_certified_bounds :",
                f"    ((log_{n}_lower : ℚ) : ℝ) ≤ Real.log ({n} : ℝ) ∧",
                f"      Real.log ({n} : ℝ) ≤ ((log_{n}_upper : ℚ) : ℝ)",
                "",
                f"noncomputable def log_{n}_interval_external_cert :",
                f"    ExternalRealIntervalCertificate (Real.log ({n} : ℝ)) := {{",
                f"  lower := log_{n}_lower",
                f"  upper := log_{n}_upper",
                "  backend := .arb",
                f"  lower_le_upper := by unfold log_{n}_lower log_{n}_upper; norm_num",
                "  certified_backend := trivial",
                f"  certified := log_{n}_certified_bounds",
                "}",
                "",
            ]
        )
    lines.extend(
        [
            vector_definition("linearCenter", 50, centers),
            "",
            "def linearLower (i : Fin 50) : ℚ := linearCenter i - (1 / 1000000000)",
            "def linearUpper (i : Fin 50) : ℚ := linearCenter i + (1 / 1000000000)",
            "",
        ]
    )
    for k in range(50):
        lines.append(interval_statement(50, k))
        lines.append("  rw [innerProductChiRho_formula]")
        lines.append("  have hgamma := euler_gamma_certified_bounds")
        if k == 0:
            lines.append(
                "  norm_num [linearLower, linearUpper, linearCenter, "
                "euler_gamma_lower, euler_gamma_upper] at hgamma ⊢"
            )
            lines.append("  constructor <;> nlinarith [hgamma.1, hgamma.2]")
        else:
            n = k + 1
            lines.append(f"  have hlog := log_{n}_certified_bounds")
            lines.append(
                "  norm_num [linearLower, linearUpper, linearCenter, "
                f"log_{n}_lower, log_{n}_upper, "
                "euler_gamma_lower, euler_gamma_upper] at hgamma hlog ⊢"
            )
            lines.append(
                "  constructor <;> nlinarith [hgamma.1, hgamma.2, hlog.1, hlog.2]"
            )
        lines.append("")
    lines.extend(
        [
            "theorem innerProductChiRho_interval (k : Fin 50) :",
            "    ((linearLower k : ℚ) : ℝ) ≤ innerProductChiRho k.val ∧",
            "      innerProductChiRho k.val ≤ ((linearUpper k : ℚ) : ℝ) := by",
            "  fin_cases k",
        ]
    )
    for k in range(50):
        lines.append(f"  · exact innerProductChiRho_interval_{k}")
    lines.extend(
        [
            "",
            "noncomputable def innerProductChiRho_certificate (k : Fin 50) :",
            "    ChiRhoIntervalCertificate k.val := {",
            "  lower := linearLower k",
            "  upper := linearUpper k",
            "  lower_le_upper := by unfold linearLower linearUpper; linarith",
            "  certified := innerProductChiRho_interval k",
            "}",
            "",
            "end RH.Certificates.Generated.ChiRhoBoundsN50",
            "",
        ]
    )
    return "\n".join(lines)


def emit_wrapper(size: int, centers: list[Fraction]) -> str:
    ns = f"RH.Certificates.Generated.ChiRhoBoundsN{size}"
    primary = "RH.Certificates.Generated.ChiRhoBoundsN50"
    lines = [
        "import RiemannHypothesis.Certificates.Generated.ChiRhoBoundsN50",
        "",
        f"/-! Generated χ–ρ interval projection for N={size}. -/",
        "",
        "set_option linter.style.longLine false",
        "",
        f"namespace {ns}",
        "",
        "open RH.Certificates",
        "",
        vector_definition("linearCenter", size, centers),
        "",
        f"def linearLower (i : Fin {size}) : ℚ := linearCenter i - (1 / 1000000000)",
        f"def linearUpper (i : Fin {size}) : ℚ := linearCenter i + (1 / 1000000000)",
        "",
    ]
    for k in range(size):
        lines.extend(
            [
                interval_statement(size, k),
                "  simpa [linearLower, linearUpper, linearCenter,",
                f"    {primary}.linearLower, {primary}.linearUpper,",
                f"    {primary}.linearCenter] using",
                f"    {primary}.innerProductChiRho_interval_{k}",
                "",
            ]
        )
    lines.extend(
        [
            f"theorem innerProductChiRho_interval (k : Fin {size}) :",
            "    ((linearLower k : ℚ) : ℝ) ≤ innerProductChiRho k.val ∧",
            "      innerProductChiRho k.val ≤ ((linearUpper k : ℚ) : ℝ) := by",
            "  fin_cases k",
        ]
    )
    for k in range(size):
        lines.append(f"  · exact innerProductChiRho_interval_{k}")
    lines.extend(
        [
            "",
            f"noncomputable def innerProductChiRho_certificate (k : Fin {size}) :",
            "    ChiRhoIntervalCertificate k.val := {",
            "  lower := linearLower k",
            "  upper := linearUpper k",
            "  lower_le_upper := by unfold linearLower linearUpper; linarith",
            "  certified := innerProductChiRho_interval k",
            "}",
            "",
            f"end {ns}",
            "",
        ]
    )
    return "\n".join(lines)


def generated_files() -> tuple[dict[str, str], dict[str, object]]:
    flint.ctx.prec = ARB_PRECISION
    centers = linear_centers()
    log_bounds = {n: outward_log_bounds(n) for n in range(2, 51)}
    audit_data(centers, log_bounds)
    files = {
        "ChiRhoBoundsN50.lean": emit_primary(centers, log_bounds),
        "ChiRhoBoundsN20.lean": emit_wrapper(20, centers),
        "ChiRhoBoundsN10.lean": emit_wrapper(10, centers),
    }
    metadata = {
        "backend": "python-flint (Arb)",
        "precision_bits": ARB_PRECISION,
        "log_denominator": LOG_DENOMINATOR,
        "linear_radius": [LINEAR_RADIUS.numerator, LINEAR_RADIUS.denominator],
        "logs": {
            str(n): {
                "lower": [bounds[0].numerator, bounds[0].denominator],
                "upper": [bounds[1].numerator, bounds[1].denominator],
            }
            for n, bounds in log_bounds.items()
        },
    }
    canonical = json.dumps(metadata, sort_keys=True).encode()
    metadata["sha256"] = hashlib.sha256(canonical).hexdigest()
    return files, metadata


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--stdout", choices=("10", "20", "50"))
    parser.add_argument("--metadata", action="store_true")
    parser.add_argument("--check", action="store_true")
    args = parser.parse_args()
    files, metadata = generated_files()
    if args.stdout:
        print(files[f"ChiRhoBoundsN{args.stdout}.lean"], end="")
        return
    if args.metadata:
        print(json.dumps(metadata, indent=2, sort_keys=True))
        return
    if args.check:
        for name, contents in files.items():
            assert (GENERATED / name).read_text() == contents
        assert json.loads((GENERATED / "chi_rho_bounds.json").read_text()) == metadata
        print("χ–ρ generated files are current and all Arb enclosure audits pass.")
        return
    GENERATED.mkdir(parents=True, exist_ok=True)
    for name, contents in files.items():
        (GENERATED / name).write_text(contents)
    (GENERATED / "chi_rho_bounds.json").write_text(
        json.dumps(metadata, indent=2, sort_keys=True) + "\n"
    )
    print("Generated ChiRhoBoundsN10/N20/N50 and chi_rho_bounds.json.")


if __name__ == "__main__":
    main()
